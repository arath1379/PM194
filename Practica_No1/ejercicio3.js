/* crea un arrow function llamada saludoPersonalizado que reciba dos parametros: nombre y edad
y retorne una cadema como la siguiente */

//"Hola, me llamo Isay y tengo 37 a単os"

const saludoPersonalizado = (nombre, edad) => `Hola, me llamo ${nombre} y tengo ${edad} a単os.`;

console.log(saludoPersonalizado("Isay", 37)); // Ejemplo: imprime "Hola, me llamo Isay y tengo 37 a単os"
